export const getCurrentThemeColors = () => {
        const root = document.documentElement;
        const computedStyle = getComputedStyle(root);
        return {
                // Primary colors
                primary: computedStyle.getPropertyValue('--p'),
                primaryFocus: computedStyle.getPropertyValue('--pf'),
                primaryContent: computedStyle.getPropertyValue('--pc'),

                // Secondary colors
                secondary: computedStyle.getPropertyValue('--s'),
                secondaryFocus: computedStyle.getPropertyValue('--sf'),
                secondaryContent: computedStyle.getPropertyValue('--sc'),

                // Accent colors
                accent: computedStyle.getPropertyValue('--a'),
                accentFocus: computedStyle.getPropertyValue('--af'),
                accentContent: computedStyle.getPropertyValue('--ac'),

                // Neutral colors
                neutral: computedStyle.getPropertyValue('--n'),
                neutralFocus: computedStyle.getPropertyValue('--nf'),
                neutralContent: computedStyle.getPropertyValue('--nc'),

                // Base colors
                base100: computedStyle.getPropertyValue('--b1'),
                base200: computedStyle.getPropertyValue('--b2'),
                base300: computedStyle.getPropertyValue('--b3'),
                baseContent: computedStyle.getPropertyValue('--bc'),

                // State colors
                info: computedStyle.getPropertyValue('--in'),
                infoContent: computedStyle.getPropertyValue('--inc'),
                success: computedStyle.getPropertyValue('--su'),
                successContent: computedStyle.getPropertyValue('--suc'),
                warning: computedStyle.getPropertyValue('--wa'),
                warningContent: computedStyle.getPropertyValue('--wac'),
                error: computedStyle.getPropertyValue('--er'),
                errorContent: computedStyle.getPropertyValue('--erc')
        };
};

